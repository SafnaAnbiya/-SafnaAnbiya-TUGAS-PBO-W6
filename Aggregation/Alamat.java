package Aggregation;

class Alamat {
	int noRumah;
	String Jalan;
	String Kecamatan;
	String Kabupaten;
	String Provinsi;
	Alamat( int noRumah, String Jalan, String Kecamatan, String Kabupaten, String Provinsi){
		this.noRumah = noRumah;
		this.Jalan = Jalan;
		this.Kecamatan = Kecamatan;
		this.Kabupaten = Kabupaten;
		this.Provinsi = Provinsi;
	}
}

